#include <stdarg.h>
#include <string.h>
#include <stdio.h>

#include "uart1n_a31g12x.h"

#include "modbus_hal.h"

//----------------------------------
// define
//----------------------------------
#define  UartModBus     11


//----------------------------------
// variablee
//----------------------------------
char rx_buf_mod[20];
char tx_buf_mod[20];

//----------------------------------
// prototype
//----------------------------------


//==============================================================================
// Sub Routines
//==============================================================================

//==============================================================================
// Rrivate Function
//==============================================================================

//==============================================================================
// Public Function
//==============================================================================
void  InitModBusHal(void)
{
	UsartInitPort(UartModBus);    //Usartn10InitPort(ENABLE);
	UsartInitBuff(UartModBus, rx_buf_mod, sizeof(rx_buf_mod), tx_buf_mod ,  sizeof(tx_buf_mod ));
	UsartOpen(UartModBus,  38400, USART_DATA_BIT_8, USART_PARITY_BIT_NONE, USART_STOP_BIT_1);
}

